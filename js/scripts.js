$(document).ready(function() {
  $("#js-hid, #js-show").click(function(){
    $("#js-show").toggle();
    $("#js-hid").toggle();
  });
  $("#op-hid, #op-show").click(function(){
    $("#op-show").toggle();
    $("#op-hid").toggle();
  });
  $("#var-hid, #var-show").click(function(){
    $("#var-show").toggle();
    $("#var-hid").toggle();
  });
});
